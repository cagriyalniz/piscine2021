#include <unistd.h>

void	ft_print_numbers(void)
{
	char	ilk;
	int		i;

	i = 0;
	ilk = 48;
	while (i <= 9)
	{
		write(1, &ilk, 1);
		ilk++;
		i++;
	}
}
